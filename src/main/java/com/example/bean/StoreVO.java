package com.example.bean;

public class StoreVO {
    private int itemID;
    private String itemName;
    private int itemPrice;
    private int itemStock;
    private int itemKind;
    private int itemCalorie;
    private String itemEvent;
    private String itemDetail;
    private String itemFile;

    public int getItemID() {
        return itemID;
    }

    public void setItemID(int itemID) {
        this.itemID = itemID;
    }

    public String getItemName() {
        return itemName;
    }

    public void setItemName(String itemName) {
        this.itemName = itemName;
    }

    public int getItemPrice() {
        return itemPrice;
    }

    public void setItemPrice(int itemPrice) {
        this.itemPrice = itemPrice;
    }

    public int getItemStock() {
        return itemStock;
    }

    public void setItemStock(int itemStock) {
        this.itemStock = itemStock;
    }

    public int getItemKind() {
        return itemKind;
    }

    public void setItemKind(int itemKind) {
        this.itemKind = itemKind;
    }

    public int getItemCalorie() {
        return itemCalorie;
    }

    public void setItemCalorie(int itemCalorie) {
        this.itemCalorie = itemCalorie;
    }

    public String getItemEvent() {
        return itemEvent;
    }

    public void setItemEvent(String itemEvent) {
        this.itemEvent = itemEvent;
    }

    public String getItemDetail() {
        return itemDetail;
    }

    public void setItemDetail(String itemDetail) {
        this.itemDetail = itemDetail;
    }

    public String getItemFile() {
        return itemFile;
    }

    public void setItemFile(String itemFile) {
        this.itemFile = itemFile;
    }
}
